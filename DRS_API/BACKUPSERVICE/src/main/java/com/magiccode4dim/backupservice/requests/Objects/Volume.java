package com.magiccode4dim.backupservice.requests.Objects;

public class Volume {
    private String nome;
    private String label;

    public Volume() {
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getLabel() {
        return label;
    }
    public void setLabel(String label) {
        this.label = label;
    }


    
}
